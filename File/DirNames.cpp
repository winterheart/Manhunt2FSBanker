std::map<uint32_t, std::string> Crc32ResolveNames::m_crc32map_scripted;
std::map<uint32_t, std::string> Crc32ResolveNames::m_crc32map_executions;

Crc32ResolveNames::Crc32ResolveNames() {
  for (const std::string &level_name : LevelNames) {
    for (const std::string &speech_name : SpeechNames) {
      std::string full_path = std::format(R"(scripted\{0}\{1}\pc_stream.wav)", level_name, speech_name);
      m_crc32map_scripted.insert({UTILS::crc32(full_path), full_path});
    }
  }

  for (const std::string &execution_name : ExecutionNames) {
    std::string name = execution_name;
    if (name.size() > 8) {
      name.resize(8);
    }
    std::transform(name.begin(), name.end(), name.begin(),
      [](const unsigned char c) {
        return std::tolower(c);
    });

    for (const std::string &action_name : ActionNames) {
      std::string full_path = std::format(R"(executions\{0}\{1}.wav)", name, action_name);
      m_crc32map_executions.insert({UTILS::crc32(full_path), full_path});
    }
  }
}

